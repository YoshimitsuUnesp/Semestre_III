public class Gato extends Animal{
    private int patas;    
    private int cauda;
    private int listras;
    
    public int getPatas() {
        return patas;
    }
    public void setPatas(int patas) {
        this.patas = patas;
    }
    public int getCauda() {
        return cauda;
    }
    public void setCauda(int cauda) {
        this.cauda = cauda;
    }
    public int getListras() {
        return listras;
    }
    public void setListras(int listras) {
        this.listras = listras;
    }
}
