public class Papagaio extends Animal{
    private int garras;
    private int penas;
    
    public int getGarras() {
        return garras;
    }
    public void setGarras(int garras) {
        this.garras = garras;
    }
    public int getPenas() {
        return penas;
    }
    public void setPenas(int penas) {
        this.penas = penas;
    }
}
