public class Cachorro extends Animal {
    private int patas;
    private int cauda;
    private int manchas;
    
    public int getPatas() {
        return patas;
    }
    public void setPatas(int patas) {
        this.patas = patas;
    }
    public int getCauda() {
        return cauda;
    }
    public void setCauda(int cauda) {
        this.cauda = cauda;
    }
    public int getManchas() {
        return manchas;
    }
    public void setManchas(int manchas) {
        this.manchas = manchas;
    }
}
